package com.entity;

public class Person {
	private String ten;
	private int tuoi;
	private int namSinh;
	private Address queQuan;
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	public int getTuoi() {
		return tuoi;
	}
	public void setTuoi(int tuoi) {
		this.tuoi = tuoi;
	}
	public int getNamSinh() {
		return namSinh;
	}
	public void setNamSinh(int namSinh) {
		this.namSinh = namSinh;
	}
	public Address getQueQuan() {
		return queQuan;
	}
	public void setQueQuan(Address queQuan) {
		this.queQuan = queQuan;
	}
	
}
